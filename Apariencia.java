package Taller1;

public class Apariencia {
	private String name;
	private String rarity;
	
	public Apariencia(String name, String rarity) {
		this.name=name;
		this.rarity=rarity;
	}
	
	public String getName() {
		return this.name;
	}
	public String getRarity() {
		return this.rarity;
	}
}
